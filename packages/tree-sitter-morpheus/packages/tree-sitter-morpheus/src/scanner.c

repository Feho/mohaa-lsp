#include <tree_sitter/parser.h>
#include <stdbool.h>
#include <wctype.h>

enum TokenType {
  LINE_CONTINUATION,
};

void *tree_sitter_morpheus_external_scanner_create(void) {
  return NULL;
}

void tree_sitter_morpheus_external_scanner_destroy(void *payload) {
}

void tree_sitter_morpheus_external_scanner_reset(void *payload) {
}

unsigned tree_sitter_morpheus_external_scanner_serialize(void *payload, char *buffer) {
  return 0;
}

void tree_sitter_morpheus_external_scanner_deserialize(void *payload, const char *buffer, unsigned length) {
}

bool tree_sitter_morpheus_external_scanner_scan(
  void *payload,
  TSLexer *lexer,
  const bool *valid_symbols
) {
  if (!valid_symbols[LINE_CONTINUATION]) {
    return false;
  }

  // Example: backslash + newline = line continuation
  if (lexer->lookahead == '\\') {
    lexer->advance(lexer, false);

    if (lexer->lookahead == '\n' || lexer->lookahead == '\r') {
      lexer->advance(lexer, false);
      lexer->result_symbol = LINE_CONTINUATION;
      return true;
    }
  }

  return false;
}
